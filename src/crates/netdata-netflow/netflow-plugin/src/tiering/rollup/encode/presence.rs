use super::super::*;
use super::push_field_id;

pub(super) fn push_presence_field_ids(
    index: &mut FlowIndex,
    rec: &FlowRecord,
    scratch_field_ids: &mut Vec<u32>,
) -> Result<(), FlowIndexError> {
    push_field_id(
        index,
        scratch_field_ids,
        56,
        IndexFieldValue::U8(u8::from(rec.has_direction())),
    )?;
    push_field_id(
        index,
        scratch_field_ids,
        57,
        IndexFieldValue::U8(u8::from(rec.has_etype())),
    )?;
    push_field_id(
        index,
        scratch_field_ids,
        58,
        IndexFieldValue::U8(u8::from(rec.has_forwarding_status())),
    )?;
    push_field_id(
        index,
        scratch_field_ids,
        59,
        IndexFieldValue::U8(u8::from(rec.has_iptos())),
    )?;
    push_field_id(
        index,
        scratch_field_ids,
        60,
        IndexFieldValue::U8(u8::from(rec.has_tcp_flags())),
    )?;
    push_field_id(
        index,
        scratch_field_ids,
        61,
        IndexFieldValue::U8(u8::from(rec.has_icmpv4_type())),
    )?;
    push_field_id(
        index,
        scratch_field_ids,
        62,
        IndexFieldValue::U8(u8::from(rec.has_icmpv4_code())),
    )?;
    push_field_id(
        index,
        scratch_field_ids,
        63,
        IndexFieldValue::U8(u8::from(rec.has_icmpv6_type())),
    )?;
    push_field_id(
        index,
        scratch_field_ids,
        64,
        IndexFieldValue::U8(u8::from(rec.has_icmpv6_code())),
    )?;
    push_field_id(
        index,
        scratch_field_ids,
        65,
        IndexFieldValue::U8(u8::from(rec.has_in_if_speed())),
    )?;
    push_field_id(
        index,
        scratch_field_ids,
        66,
        IndexFieldValue::U8(u8::from(rec.has_out_if_speed())),
    )?;
    push_field_id(
        index,
        scratch_field_ids,
        67,
        IndexFieldValue::U8(u8::from(rec.has_in_if_boundary())),
    )?;
    push_field_id(
        index,
        scratch_field_ids,
        68,
        IndexFieldValue::U8(u8::from(rec.has_out_if_boundary())),
    )?;
    push_field_id(
        index,
        scratch_field_ids,
        69,
        IndexFieldValue::U8(u8::from(rec.has_src_vlan())),
    )?;
    push_field_id(
        index,
        scratch_field_ids,
        70,
        IndexFieldValue::U8(u8::from(rec.has_dst_vlan())),
    )
}
