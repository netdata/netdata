use super::super::*;
use super::push_field_id;

pub(super) fn push_interface_field_ids(
    index: &mut FlowIndex,
    rec: &FlowRecord,
    scratch_field_ids: &mut Vec<u32>,
) -> Result<(), FlowIndexError> {
    push_field_id(
        index,
        scratch_field_ids,
        24,
        IndexFieldValue::U32(rec.in_if),
    )?;
    push_field_id(
        index,
        scratch_field_ids,
        25,
        IndexFieldValue::U32(rec.out_if),
    )?;
    push_field_id(
        index,
        scratch_field_ids,
        26,
        IndexFieldValue::Text(rec.in_if_name.as_str()),
    )?;
    push_field_id(
        index,
        scratch_field_ids,
        27,
        IndexFieldValue::Text(rec.out_if_name.as_str()),
    )?;
    push_field_id(
        index,
        scratch_field_ids,
        28,
        IndexFieldValue::Text(rec.in_if_description.as_str()),
    )?;
    push_field_id(
        index,
        scratch_field_ids,
        29,
        IndexFieldValue::Text(rec.out_if_description.as_str()),
    )?;
    push_field_id(
        index,
        scratch_field_ids,
        30,
        IndexFieldValue::U64(rec.in_if_speed),
    )?;
    push_field_id(
        index,
        scratch_field_ids,
        31,
        IndexFieldValue::U64(rec.out_if_speed),
    )?;
    push_field_id(
        index,
        scratch_field_ids,
        32,
        IndexFieldValue::Text(rec.in_if_provider.as_str()),
    )?;
    push_field_id(
        index,
        scratch_field_ids,
        33,
        IndexFieldValue::Text(rec.out_if_provider.as_str()),
    )?;
    push_field_id(
        index,
        scratch_field_ids,
        34,
        IndexFieldValue::Text(rec.in_if_connectivity.as_str()),
    )?;
    push_field_id(
        index,
        scratch_field_ids,
        35,
        IndexFieldValue::Text(rec.out_if_connectivity.as_str()),
    )?;
    push_field_id(
        index,
        scratch_field_ids,
        36,
        IndexFieldValue::U8(rec.in_if_boundary),
    )?;
    push_field_id(
        index,
        scratch_field_ids,
        37,
        IndexFieldValue::U8(rec.out_if_boundary),
    )
}
