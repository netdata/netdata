var contextSuffixRE = regexp.MustCompile(`^[a-z0-9][a-z0-9_.]*$`)

func validateMetrics(metrics []MetricConfig, idx *ProfileIndex) error {
	if idx == nil {
		return errors.New("profile index not available")
	}
	seenOIDs := make(map[string]struct{}, len(metrics))
	seenResolvedOIDs := make(map[string]struct{}, len(metrics))
	seenContexts := make(map[string]struct{}, len(metrics))
	seenSelectors := make(map[string]string, len(metrics))
	for i, m := range metrics {
		prefix := fmt.Sprintf("metrics[%d]", i)
		if m.OID == "" {
			return fmt.Errorf("%s: oid is required", prefix)
		}
		if !isNumericOID(m.OID) {
			return fmt.Errorf("%s: invalid oid %q", prefix, m.OID)
		}
		if _, ok := seenOIDs[m.OID]; ok {
			return fmt.Errorf("%s: duplicate oid %q", prefix, m.OID)
		}
		seenOIDs[m.OID] = struct{}{}

		td := idx.Lookup(m.OID)
		if td == nil {
			return fmt.Errorf("%s: oid %q not found in any loaded profile", prefix, m.OID)
		}
		resolvedOID := td.OID
		if resolvedOID == "" {
			resolvedOID = m.OID
		}
		if _, ok := seenResolvedOIDs[resolvedOID]; ok {
			return fmt.Errorf("%s: duplicate oid %q resolves to already configured trap oid %q", prefix, m.OID, resolvedOID)
		}
		seenResolvedOIDs[resolvedOID] = struct{}{}

		if m.Context == "" {
			return fmt.Errorf("%s: context is required", prefix)
		}
		const requiredPrefix = "snmp.trap."
		if !strings.HasPrefix(m.Context, requiredPrefix) {
			return fmt.Errorf("%s: context %q must start with %q", prefix, m.Context, requiredPrefix)
		}
		suffix := m.Context[len(requiredPrefix):]
		if suffix == "" {
			return fmt.Errorf("%s: context %q has no suffix after %q", prefix, m.Context, requiredPrefix)
		}
		if !contextSuffixRE.MatchString(suffix) {
			return fmt.Errorf("%s: context %q has invalid suffix %q (must be lowercase, digits, underscore, dot)", prefix, m.Context, suffix)
		}
		if strings.HasSuffix(suffix, ".") || strings.Contains(suffix, "..") {
			return fmt.Errorf("%s: context %q has invalid suffix %q (dot separators must not be empty)", prefix, m.Context, suffix)
		}
		if _, ok := seenContexts[m.Context]; ok {
			return fmt.Errorf("%s: duplicate context %q", prefix, m.Context)
		}
		seenContexts[m.Context] = struct{}{}
		selector := metricSelector(m.Context)
		if previousContext, ok := seenSelectors[selector]; ok {
			return fmt.Errorf("%s: context %q generates duplicate metric selector %q already generated by context %q", prefix, m.Context, selector, previousContext)
		}
		seenSelectors[selector] = m.Context

		if m.DimensionFromVarbind != "" {
			if err := validateMetricDimensionVarbind(prefix, td, m.DimensionFromVarbind); err != nil {
				return err
			}
		}
	}
	return nil
}

func validateMetricDimensionVarbind(prefix string, td *TrapDef, varbindName string) error {
	if isNumericOID(varbindName) {
		return fmt.Errorf("%s: dimension_from_varbind %q is a raw numeric OID; use a symbolic varbind name instead", prefix, varbindName)
	}

	vb := td.varbindByName(varbindName)
	if vb == nil {
		vb = td.inlineVarbindByName(varbindName)
	}
	if vb == nil {
		return fmt.Errorf("%s: dimension_from_varbind %q not found in trap definition %q varbind references", prefix, varbindName, td.OID)
	}

	if _, ok := metricDimensionBoundFromVarbind(vb); !ok {
		return fmt.Errorf("%s: dimension_from_varbind %q references unbounded varbind (type %q); only enum-backed with <= %d values, boolean/truthvalue, or numeric range with <= %d values are allowed", prefix, varbindName, vb.Type, maxBoundedVarbindValues, maxBoundedVarbindValues)
	}

	return nil
}

type operatorMetric struct {
	context              string
	dimensionFromVarbind string
	dimensionBound       metricDimensionBound
	selector             string

	singleCount atomic.Uint64
	dimMu       sync.Mutex
	dimCounts   map[string]*atomic.Uint64
}

type operatorMetrics struct {
	metrics []operatorMetric
	byOID   map[string]int
}

func newOperatorMetrics(configs []MetricConfig, idx *ProfileIndex) *operatorMetrics {
	om := &operatorMetrics{
		metrics: make([]operatorMetric, len(configs)),
		byOID:   make(map[string]int, len(configs)),
	}
	for i, cfg := range configs {
		selector := metricSelector(cfg.Context)
		var td *TrapDef
		if idx != nil {
			td = idx.Lookup(cfg.OID)
		}
		om.metrics[i] = operatorMetric{
			context:              cfg.Context,
			dimensionFromVarbind: cfg.DimensionFromVarbind,
			dimensionBound:       metricDimensionBoundFromTrap(td, cfg.DimensionFromVarbind),
			selector:             selector,
		}
		for _, oid := range metricOIDAliases(cfg.OID, idx) {
			om.byOID[oid] = i
		}
	}
	return om
}

func metricOIDAliases(oid string, idx *ProfileIndex) []string {
	aliases := []string{oid}
	if idx == nil {
		return aliases
	}
	td := idx.Lookup(oid)
	if td != nil && td.OID != "" && !slices.Contains(aliases, td.OID) {
		aliases = append(aliases, td.OID)
	}
	if alt := alternateTrapOID(oid); alt != oid {
		if idx.Lookup(alt) == td && !slices.Contains(aliases, alt) {
			aliases = append(aliases, alt)
		}
	}
	if td != nil && td.OID != "" {
		if alt := alternateTrapOID(td.OID); alt != td.OID {
			if idx.Lookup(alt) == td && !slices.Contains(aliases, alt) {
				aliases = append(aliases, alt)
			}
		}
	}
	return aliases
}

func metricContextSuffix(context string) string {
	return strings.TrimPrefix(context, "snmp.trap.")
}

func metricSelector(context string) string {
	return "snmp_trap_metric_" + strings.ReplaceAll(metricContextSuffix(context), ".", "_")
}

func metricChartID(context string) string {
	return "metric_" + strings.ReplaceAll(metricContextSuffix(context), ".", "_")
}

func (om *operatorMetrics) inc(oid string, entry *TrapEntry, td *TrapDef) {
	if om == nil || entry == nil || td == nil {
		return
	}
	idx, ok := om.byOID[oid]
	if !ok {
		return
	}
	m := &om.metrics[idx]
	if m.dimensionFromVarbind == "" {
		m.singleCount.Add(1)
		return
	}

	dimValue := resolveMetricDimValue(entry, td, m.dimensionFromVarbind, m.dimensionBound)

	m.dimMu.Lock()
	if m.dimCounts == nil {
		m.dimCounts = make(map[string]*atomic.Uint64)
	}
	ctr, ok := m.dimCounts[dimValue]
	if !ok {
		ctr = &atomic.Uint64{}
		m.dimCounts[dimValue] = ctr
	}
	m.dimMu.Unlock()
	ctr.Add(1)
}

func resolveMetricDimValue(entry *TrapEntry, td *TrapDef, varbindName string, bound metricDimensionBound) string {
	vb := td.varbindByName(varbindName)
	if vb == nil {
		vb = td.inlineVarbindByName(varbindName)
	}
	if vb == nil {
		return "<missing>"
	}

	if v, ok := findVarbindForProfileOID(entry, vb.OID); ok {
		return bound.Value(v, vb)
	}
	return "<missing>"
}

type metricDimensionKind uint8

const (
	metricDimensionUnknown metricDimensionKind = iota
	metricDimensionEnum
	metricDimensionBool
	metricDimensionRange
)

type metricDimensionBound struct {
	kind metricDimensionKind
	enum map[string]string
	min  int64
	max  int64
}

func metricDimensionBoundFromTrap(td *TrapDef, varbindName string) metricDimensionBound {
	if td == nil || varbindName == "" {
		return metricDimensionBound{}
	}
	vb := td.varbindByName(varbindName)
	if vb == nil {
		vb = td.inlineVarbindByName(varbindName)
	}
	if bound, ok := metricDimensionBoundFromVarbind(vb); ok {
		return bound
	}
	return metricDimensionBound{}
}

func metricDimensionBoundFromVarbind(vb *VarbindDef) (metricDimensionBound, bool) {
	if vb == nil {
		return metricDimensionBound{}, false
	}
	if len(vb.Enum) > 0 {
		if len(vb.Enum) > maxBoundedVarbindValues {
			return metricDimensionBound{}, false
		}
		enum := make(map[string]string, len(vb.Enum))
		maps.Copy(enum, vb.Enum)
		return metricDimensionBound{kind: metricDimensionEnum, enum: enum}, true
	}
	switch strings.ToLower(vb.Type) {
	case "boolean", "truthvalue":
		return metricDimensionBound{kind: metricDimensionBool}, true
	}
	if n, ok := numericRangeCardinality(vb.Constraints); ok {
		if n <= 0 || n > maxBoundedVarbindValues {
			return metricDimensionBound{}, false
		}
		minVal, maxVal, _ := numericRangeBounds(vb.Constraints)
		return metricDimensionBound{kind: metricDimensionRange, min: minVal, max: maxVal}, true
	}
	return metricDimensionBound{}, false
}

func (b metricDimensionBound) Value(v VarbindValue, vb *VarbindDef) string {
	if vb == nil {
		return "<missing>"
	}
	switch b.kind {
	case metricDimensionEnum:
		if len(vb.Enum) == 0 {
			return "unknown"
		}
		key := fmt.Sprintf("%v", v.Value)
		label, ok := b.enum[key]
		if !ok {
			return "unknown"
		}
		if _, ok := vb.Enum[key]; !ok {
			return "unknown"
		}
		return label
	case metricDimensionBool:
		switch strings.ToLower(vb.Type) {
		case "boolean", "truthvalue":
			if val, ok := metricBoolValue(v.Value); ok {
				return val
			}
		}
		return "unknown"
	case metricDimensionRange:
		currentMin, currentMax, ok := numericRangeBounds(vb.Constraints)
		if !ok {
			return "unknown"
		}
		val, ok := metricIntValue(v.Value)
		if ok && val >= b.min && val <= b.max && val >= currentMin && val <= currentMax {
			return strconv.FormatInt(val, 10)
		}
		return "out_of_range"
	default:
		return "unknown"
	}
}

func metricBoolValue(value any) (string, bool) {
	switch v := value.(type) {
	case bool:
		if v {
			return "true", true
		}
		return "false", true
	case int64:
		switch v {
		case 1:
			return "true", true
		case 2:
			return "false", true
		}
	case uint64:
		switch v {
		case 1:
			return "true", true
		case 2:
			return "false", true
		}
	case string:
		switch strings.ToLower(strings.TrimSpace(v)) {
		case "true", "1":
			return "true", true
		case "false", "2":
			return "false", true
		}
	}
	return "", false
}

func metricIntValue(value any) (int64, bool) {
	switch v := value.(type) {
	case int64:
		return v, true
	case uint64:
		if v <= uint64(^uint64(0)>>1) {
			return int64(v), true
		}
	case string:
		parsed, err := strconv.ParseInt(strings.TrimSpace(v), 10, 64)
		return parsed, err == nil
	}
	return 0, false
}

func (om *operatorMetrics) collect(store metrix.CollectorStore, jobName string) {
	if om == nil {
		return
	}
	meter := store.Write().SnapshotMeter("").WithLabels(metrix.Label{Key: "job_name", Value: jobName})
	for i := range om.metrics {
		m := &om.metrics[i]
		if m.dimensionFromVarbind == "" {
			meter.Counter(m.selector).ObserveTotal(metrix.SampleValue(m.singleCount.Load()))
			continue
		}

		m.dimMu.Lock()
		dimSnapshot := make(map[string]*atomic.Uint64, len(m.dimCounts))
		maps.Copy(dimSnapshot, m.dimCounts)
		m.dimMu.Unlock()

		for dimVal, ctr := range dimSnapshot {
			meter.Counter(m.selector).ObserveTotal(
				metrix.SampleValue(ctr.Load()),
				meter.LabelSet(metrix.Label{Key: "varbind_value", Value: dimVal}),
			)
		}
	}
}

func buildOperatorMetricChartTemplate(metrics []MetricConfig) (charttplMetrics []string, charts []charttpl.Chart) {
	for _, m := range metrics {
		selector := metricSelector(m.Context)
		charttplMetrics = append(charttplMetrics, selector)

		suffix := metricContextSuffix(m.Context)
		chart := charttpl.Chart{
			ID:      metricChartID(m.Context),
			Title:   fmt.Sprintf("SNMP trap metric: %s", suffix),
			Context: suffix,
			Units:   "events/s",
			Type:    "line",
			Instances: &charttpl.Instances{
				ByLabels: []string{"job_name"},
			},
		}

		if m.DimensionFromVarbind != "" {
			chart.Dimensions = []charttpl.Dimension{{
				Selector:      selector,
				NameFromLabel: "varbind_value",
			}}
		} else {
			chart.Dimensions = []charttpl.Dimension{{
				Selector: selector,
				Name:     "events",
			}}
		}

		charts = append(charts, chart)
	}
	return
}

func buildChartTemplateYAML(metrics []MetricConfig) (string, error) {
	spec, err := charttpl.DecodeYAML([]byte(chartTemplateYAML))
	if err != nil {
		return "", fmt.Errorf("failed to decode base chart template: %w", err)
	}
	if len(spec.Groups) == 0 {
		return "", fmt.Errorf("base chart template has no groups")
	}

	if len(metrics) > 0 {
		opMetrics, opCharts := buildOperatorMetricChartTemplate(metrics)
		group := &spec.Groups[0]
		group.Metrics = append(group.Metrics, opMetrics...)
		group.Charts = append(group.Charts, opCharts...)
	}
	if err := spec.Validate(); err != nil {
		return "", fmt.Errorf("invalid chart template: %w", err)
	}

	raw, err := yaml.Marshal(spec)
	if err != nil {
		return "", fmt.Errorf("failed to marshal chart template: %w", err)
	}
	return string(raw), nil
}
